<template>
<div class="container">

    <div class="row">
        <div class="col">
            <input type="text" class="form-control" placeholder="Search all tasks" v-model="filter.text">
        </div>
        <div class="col">
            <select class="form-control" v-model="filter.user_id">
                <option :value="null">User</option>
                <option v-for="user in project.team" :value="user.id">{{ user.name }}</option>
            </select>
        </div>
        <div class="col">
            <div class="btn btn-primary btn-sm float-right" @click="create()">
                Create task
            </div>
        </div>
    </div>

    <ul class="list-group mt-3">
        <li class="list-group-item">
            <div class="row">
                <div class="col">

                </div>
                <div class="col-3">
                    Assignees
                </div>
            </div>
        </li>
        <li class="list-group-item" v-for="task in tasks">
            <div class="row">
                <div class="col">
                    <router-link :to="{ name: 'project-view-task-view', params: { task_id: task.id }}" class="h4">{{ task.title }}</router-link>
                </div>
                <div class="col-3">
                    <span v-for="user in task.team">
                        <img class="img-fluid avatar assignee" :src="user.image_url" :title="user.name">
                    </span>
                </div>
            </div>
        </li>
    </ul>

</div>
</template>

<script>

    export default {

        data ()
        {
            return {
                filter: {
                    text: '',
                    user_id: null
                }
            }
        },

        computed: {

            project ()
            {
                return this.$store.state.project.project
            },

            tasks ()
            {
                return this.$store.getters['project/filterTasks'](this.filter)
            }

        },

        methods: {

            create ()
            {
                this.$router.push({ name: 'project-view-task-create' })
            },

            view (task_id)
            {
                this.$router.push({ name: 'project-view-task-view', params: { task_id: task_id }})
            }

        }

    }

</script>
